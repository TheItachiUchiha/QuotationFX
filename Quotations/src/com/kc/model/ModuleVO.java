package com.kc.model;

public class ModuleVO {

	
	private boolean quotation;

	private boolean priceEstimation;

	private boolean salesorderManagement;

	private boolean statusReminder;

	private boolean report;

	/**
	 * @return the quotation
	 */
	public boolean isQuotation() {
		return quotation;
	}

	/**
	 * @param quotation the quotation to set
	 */
	public void setQuotation(boolean quotation) {
		this.quotation = quotation;
	}

	/**
	 * @return the priceEstimation
	 */
	public boolean isPriceEstimation() {
		return priceEstimation;
	}

	/**
	 * @param priceEstimation the priceEstimation to set
	 */
	public void setPriceEstimation(boolean priceEstimation) {
		this.priceEstimation = priceEstimation;
	}

	/**
	 * @return the salesorderManagement
	 */
	public boolean isSalesorderManagement() {
		return salesorderManagement;
	}

	/**
	 * @param salesorderManagement the salesorderManagement to set
	 */
	public void setSalesorderManagement(boolean salesorderManagement) {
		this.salesorderManagement = salesorderManagement;
	}

	/**
	 * @return the statusReminder
	 */
	public boolean isStatusReminder() {
		return statusReminder;
	}

	/**
	 * @param statusReminder the statusReminder to set
	 */
	public void setStatusReminder(boolean statusReminder) {
		this.statusReminder = statusReminder;
	}

	/**
	 * @return the report
	 */
	public boolean isReport() {
		return report;
	}

	/**
	 * @param report the report to set
	 */
	public void setReport(boolean report) {
		this.report = report;
	}
	
	
	
	
}
