package com.kc.model;

import java.io.File;

public class HelpVO {

	private int id;
	
	private String name;
	
	private String description;
	
	private String address;
	
	private String contact;
	
	private File companyLogo;
	
	private File homeLogo;
	
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}

	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}

	/**
	 * @return the address
	 */
	public String getAddress() {
		return address;
	}

	/**
	 * @param address the address to set
	 */
	public void setAddress(String address) {
		this.address = address;
	}

	/**
	 * @return the contact
	 */
	public String getContact() {
		return contact;
	}

	/**
	 * @param contact the contact to set
	 */
	public void setContact(String contact) {
		this.contact = contact;
	}

	/**
	 * @return the companyLogo
	 */
	public File getCompanyLogo() {
		return companyLogo;
	}

	/**
	 * @param companyLogo the companyLogo to set
	 */
	public void setCompanyLogo(File companyLogo) {
		this.companyLogo = companyLogo;
	}

	/**
	 * @return the homeLogo
	 */
	public File getHomeLogo() {
		return homeLogo;
	}

	/**
	 * @param homeLogo the homeLogo to set
	 */
	public void setHomeLogo(File homeLogo) {
		this.homeLogo = homeLogo;
	}

	/**
	 * @return the companyLogo
	 */
	
}
